func BenchmarkAlign_buildCallPut(b *testing.B) {
	cases := []struct {
		name    string
		options []rainbow.Option
	}{
		{"Psy", psyOptions},
		{"Deribit", deribitOptions},
	}

	for _, c := range cases {
		b.Run(c.name, func(b *testing.B) {
			for i := 1; i < b.N; i++ {
				_ = buildCallPut(c.options)
			}
		})
	}
}

func TestAlign_buildCallPut(t *testing.T) {
	t.Parallel()

	cases := []struct {
		name    string
		options []rainbow.Option
		want    []Row
	}{
		{"Nil", nil, []Row{}},
		{"Empty", []rainbow.Option{}, []Row{}},
		{"Psy", psyOptions, psyRow},
		{"Deribit", deribitOptions, deribitRows},
	}

	for _, c := range cases {
		c := c

		t.Run(c.name, func(t *testing.T) {
			t.Parallel()

			got := buildCallPut(c.options)

			sort.Slice(c.want, lessRows(c.want))
			sort.Slice(got, lessRows(got))

			if !reflect.DeepEqual(c.want, got) { // DeepEqual() is twice faster than cmp.Diff()
				diff := cmp.Diff(c.want, got)
				if diff != "" {
					t.Error("Diff -want +got")
					t.Error(diff)

					wantJSON, _ := json.MarshalIndent(c.want, "", "  ")
					gotJSON, _ := json.MarshalIndent(got, "", "  ")
					t.Error("want:", string(wantJSON))
					t.Error("got: ", string(gotJSON))

					t.Errorf("%#v", got)

					t.FailNow()
				}
			}
		})
	}
}

func lessRows(rows []Row) func(p, q int) bool {
	return func(p, q int) bool { return rows[p].less(&rows[q]) }
}

